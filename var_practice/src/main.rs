fn main() {
    println!("variable practice");

    let a = 45; // immutable variable
    println!("{a}"); //first method
    println!("{}"a); // second method

    let mut b = 45; // mutable variable
    println!("{b}"); //first method
    println!("{}"b); // second method

    let c = "String : I am rust"; // immutable variable
    println!("{c}"); //first method
    println!("{}"c); // second method
}
